function timestamp() {
  return new Date().toISOString().replace(/[-:]/g, "").replace(/\..+/, "").replace("T", "-");
}

function ensureDirectory(outputDir, force) {
  fs.mkdirSync(outputDir, { recursive: true });

  const existing = fs.readdirSync(outputDir);
  if (existing.length > 0 && !force) {
    throw new Error(`Output directory is not empty: ${outputDir}. Use --force only if intentional.`);
  }
}

function walkFiles(root) {
  const files = [];

  if (!root || !fs.existsSync(root)) {
    return files;
  }

  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    const fullPath = path.join(root, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath));
      continue;
    }

    if (entry.isFile()) {
      files.push(fullPath);
    }
  }

  return files;
}

function walkDirectories(root) {
  const dirs = [];

  if (!root || !fs.existsSync(root)) {
    return dirs;
  }

  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    const fullPath = path.join(root, entry.name);
    if (entry.isDirectory()) {
      dirs.push(fullPath, ...walkDirectories(fullPath));
    }
  }

  return dirs;
}

function findDatabaseFiles(packageDir) {
  return walkFiles(packageDir)
    .filter((filePath) => {
      const lower = filePath.toLowerCase();
      const base = path.basename(lower);
      return (
        base === "caixa-agil.sqlite" ||
        base === "caixa-agil-dev.db" ||
        ((lower.endsWith(".sqlite") || lower.endsWith(".db")) && lower.includes("caixa") && lower.includes("gil"))
      );
    })
    .sort((left, right) => fs.statSync(right).size - fs.statSync(left).size);
}

function findMediaRoots(packageDir, companyId) {
  return walkDirectories(packageDir)
    .filter((dirPath) => path.basename(dirPath).toLowerCase() === "product-images")
    .filter((dirPath) => {
      const companyPath = path.join(dirPath, companyId);
      return fs.existsSync(companyPath) || walkFiles(dirPath).some((file) => /\.(png|jpe?g|webp|gif)$/i.test(file));
    });
}

function hashBuffer(buffer) {
  return crypto.createHash("sha256").update(buffer).digest("hex");
}

function hashFile(filePath) {
  return hashBuffer(fs.readFileSync(filePath));
}

function resolveNodeModule(moduleName, candidateRoots) {
  for (const root of candidateRoots) {
    try {
      return require.resolve(moduleName, { paths: [root] });
    } catch (_) {
      // Try next root.
    }
  }

  return null;
}

async function createDatabase(sqlitePath) {
  const candidateRoots = [
    process.cwd(),
    path.resolve(process.cwd(), "api"),
    path.resolve(process.cwd(), "pdv"),
    path.resolve(process.cwd(), "..", "caixa-agil-monorepo", "caixa-agil"),
    path.resolve("C:\\Pedro\\Programacao\\caixa-agil-monorepo\\caixa-agil"),
  ];

  const libsqlPath = resolveNodeModule("@libsql/client", candidateRoots);
  if (libsqlPath) {
    const libsql = await import(pathToFileURL(libsqlPath).href);
    const client = libsql.createClient({ url: pathToFileURL(sqlitePath).href });

    return {
      driver: "@libsql/client",
      async all(sql, args = []) {
        const result = await client.execute({ sql, args });
        return result.rows.map((row) => ({ ...row }));
      },
      async close() {
        client.close?.();
      },
    };
  }

  const sqlJsPath = resolveNodeModule("sql.js", candidateRoots);
  if (!sqlJsPath) {
    throw new Error("Neither @libsql/client nor sql.js was found. Run npm install in pdv or keep the old caixa-agil node_modules available.");
  }

  const sqlJs = await import(pathToFileURL(sqlJsPath).href);
  const initSqlJs = sqlJs.default || sqlJs;
  const wasmPath = path.basename(path.dirname(sqlJsPath)) === "dist"
    ? path.dirname(sqlJsPath)
    : path.join(path.dirname(sqlJsPath), "dist");
  const SQL = await initSqlJs({
    locateFile: (file) => path.join(wasmPath, file),
  });
  const database = new SQL.Database(fs.readFileSync(sqlitePath));

  return {
    driver: "sql.js",
    async all(sql, args = []) {
      const statement = database.prepare(sql);
      try {
        statement.bind(args);
        const rows = [];
        while (statement.step()) {
          rows.push(statement.getAsObject());
        }
        return rows;
      } finally {
        statement.free();
      }
    },
    async close() {
      database.close();
    },
  };
}

async function tableExists(db, tableName) {
  const rows = await db.all("SELECT name FROM sqlite_master WHERE type = 'table' AND name = ? LIMIT 1", [tableName]);
  return rows.length > 0;
}

async function tableColumns(db, tableName) {
  if (!(await tableExists(db, tableName))) {
    return [];
  }

  const rows = await db.all(`PRAGMA table_info("${tableName.replace(/"/g, '""')}")`);
  return rows.map((row) => String(row.name));
}

async function exportTable(db, tableName, companyId, outputDir) {
  const columns = await tableColumns(db, tableName);

  if (columns.length === 0) {
    return { table: tableName, skipped: true, reason: "missing_table", rowCount: 0 };
  }

  let rows = [];
  if (tableName === "empresas") {
    if (!columns.includes("id")) {
      return { table: tableName, skipped: true, reason: "missing_id", rowCount: 0 };
    }
    rows = await db.all('SELECT * FROM "empresas" WHERE "id" = ? LIMIT 1', [companyId]);
  } else if (columns.includes("empresa_id")) {
    const orderColumn = columns.includes("id") ? '"id"' : "rowid";
    rows = await db.all(`SELECT * FROM "${tableName}" WHERE "empresa_id" = ? ORDER BY ${orderColumn}`, [companyId]);
  } else {
    return { table: tableName, skipped: true, reason: "missing_empresa_id", rowCount: 0 };
  }

  const payload = {
    table: tableName,
    sourceCompanyId: companyId,
    exportedAt: new Date().toISOString(),
    rowCount: rows.length,
    rows,
  };
  const serialized = `${JSON.stringify(payload, null, 2)}\n`;
  const fileName = `${tableName}.json`;
  fs.writeFileSync(path.join(outputDir, fileName), serialized, "utf8");

  return {
    table: tableName,
    skipped: false,
    rowCount: rows.length,
    fileName,
    bytes: Buffer.byteLength(serialized),
    sha256: hashBuffer(Buffer.from(serialized)),
  };
}

async function scoreDatabase(sqlitePath, companyId) {
  const db = await createDatabase(sqlitePath);
  try {
    let score = 0;
    const counts = {};
    for (const tableName of ["produtos", "vendas", "convenios", "saldos_estoque"]) {
      const columns = await tableColumns(db, tableName);
      if (!columns.includes("empresa_id")) {
        counts[tableName] = 0;
        continue;
      }
      const rows = await db.all(`SELECT COUNT(*) AS total FROM "${tableName}" WHERE "empresa_id" = ?`, [companyId]);
      const count = Number(rows[0]?.total || 0);
      counts[tableName] = count;
      score += count;
    }
    return { sqlitePath, score, counts };
  } finally {
    await db.close();
  }
}

function copyDirectoryContents(source, destination) {
  if (!fs.existsSync(source)) {
    return 0;
  }

  let copied = 0;
  for (const entry of fs.readdirSync(source, { withFileTypes: true })) {
    const sourcePath = path.join(source, entry.name);
    const destinationPath = path.join(destination, entry.name);
    if (entry.isDirectory()) {
      fs.mkdirSync(destinationPath, { recursive: true });
      copied += copyDirectoryContents(sourcePath, destinationPath);
      continue;
    }

    if (entry.isFile()) {
      fs.mkdirSync(path.dirname(destinationPath), { recursive: true });
      fs.copyFileSync(sourcePath, destinationPath);
      copied += 1;
    }
  }

  return copied;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    printHelp();
    return;
  }

  const positionalSource = args._[0] ? path.resolve(String(args._[0])) : null;
  const positionalOutput = args._[1] ? path.resolve(String(args._[1])) : null;
  const packageDir = args["package-dir"]
    ? path.resolve(String(args["package-dir"]))
    : positionalSource && fs.existsSync(positionalSource) && fs.statSync(positionalSource).isDirectory()
      ? positionalSource
      : null;
  const companyId = String(args["company-id"] || SOURCE_COMPANY_ID);

  let sqlitePath = args.sqlite
    ? path.resolve(String(args.sqlite))
    : positionalSource && fs.existsSync(positionalSource) && fs.statSync(positionalSource).isFile()
      ? positionalSource
      : null;
  if (!sqlitePath && packageDir) {
    const candidates = findDatabaseFiles(packageDir);
    if (candidates.length === 0) {
      throw new Error(`No SQLite database was found inside ${packageDir}.`);
    }

    const scored = [];
    for (const candidate of candidates) {
      try {
        scored.push(await scoreDatabase(candidate, companyId));
      } catch (error) {
        scored.push({ sqlitePath: candidate, score: -1, error: error.message });
      }
    }
    scored.sort((left, right) => right.score - left.score);
    sqlitePath = scored[0].sqlitePath;
  }

  if (!sqlitePath || !fs.existsSync(sqlitePath)) {
    throw new Error("Informe --sqlite ou --package-dir com um banco SQLite valido.");
  }

  const outputDir = path.resolve(args.output || positionalOutput || path.join(process.cwd(), `legacy-caixa-agil-local-export-${timestamp()}`));
  ensureDirectory(outputDir, Boolean(args.force));

  const db = await createDatabase(sqlitePath);
  const summaries = [];
  try {
    for (const tableName of TABLES) {
      summaries.push(await exportTable(db, tableName, companyId, outputDir));
    }
  } finally {
    await db.close();
  }

  const mediaRoots = packageDir ? findMediaRoots(packageDir, companyId) : [];
  const mediaOutputDir = path.join(outputDir, "legacy-media", "product-images");
  let copiedMediaFiles = 0;
  for (const mediaRoot of mediaRoots) {
    copiedMediaFiles += copyDirectoryContents(mediaRoot, mediaOutputDir);
  }

  const sqliteSidecars = ["-wal", "-shm"]
    .map((suffix) => `${sqlitePath}${suffix}`)
    .filter((sidecar) => fs.existsSync(sidecar))
    .map((sidecar) => ({
      path: sidecar,
      bytes: fs.statSync(sidecar).size,
      sha256: hashFile(sidecar),
    }));

  const manifest = {
    schema: "caixaagil-legacy-local-sqlite-export.v1",
    exportedAt: new Date().toISOString(),
    sourceCompanyId: companyId,
    packageDir,
    sqlite: {
      path: sqlitePath,
      bytes: fs.statSync(sqlitePath).size,
      sha256: hashFile(sqlitePath),
      sidecars: sqliteSidecars,
      warning: sqliteSidecars.length > 0 ? "WAL/SHM sidecars were present; @libsql/client should be preferred over sql.js for latest rows." : null,
    },
    tables: summaries,
    media: {
      roots: mediaRoots,
      copiedFiles: copiedMediaFiles,
      legacyMediaDir: fs.existsSync(mediaOutputDir) ? mediaOutputDir : null,
    },
  };

  fs.writeFileSync(path.join(outputDir, "manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`, "utf8");

  console.log(`Export local gerado em: ${outputDir}`);
  console.table(
    summaries.map((summary) => ({
      tabela: summary.table,
      linhas: summary.rowCount,
      status: summary.skipped ? summary.reason : "ok",
    })),
  );
  if (manifest.media.legacyMediaDir) {
    console.log(`Midias copiadas: ${manifest.media.copiedFiles}`);
    console.log(`Use no import de imagens: --legacy-media-dir "${manifest.media.legacyMediaDir}"`);
  }
}

main().catch((error) => {
  console.error(error.stack || error.message);
  process.exit(1);
});
