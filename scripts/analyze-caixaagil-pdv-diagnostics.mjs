#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { createRequire } from "node:module";
import { pathToFileURL } from "node:url";

const require = createRequire(import.meta.url);

function parseArgs(argv) {
  const args = { _: [] };

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];

    if (!token.startsWith("--")) {
      args._.push(token);
      continue;
    }

    const key = token.slice(2);
    const next = argv[index + 1];

    if (!next || next.startsWith("--")) {
      args[key] = true;
      continue;
    }

    args[key] = next;
    index += 1;
  }

  return args;
}

function printHelp() {
  console.log(`
Usage:
  node scripts/analyze-caixaagil-pdv-diagnostics.mjs --package-dir C:\\caixaagil-resgate\\caixaagil-old-pdv-PC-YYYYMMDD-HHMMSS

Options:
  --package-dir <dir>  Directory generated by collect-caixaagil-old-pc-data.ps1.
  --sqlite <file>      Current PDV SQLite file. Optional when --package-dir is used.
  --output <file>      Optional JSON output path.
  --limit <n>          Max pending/failed records per list. Default: 100.
  --help              Show this help.
`);
}

function walkFiles(root) {
  const files = [];

  if (!root || !fs.existsSync(root)) {
    return files;
  }

  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    const fullPath = path.join(root, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }

  return files;
}

function resolveNodeModule(moduleName, candidateRoots) {
  for (const root of candidateRoots) {
    try {
      return require.resolve(moduleName, { paths: [root] });
    } catch (_) {
      // Try next root.
    }
  }

  return null;
}

async function createSqlJsDatabase(sqlitePath) {
  const candidateRoots = [
    process.cwd(),
    path.resolve(process.cwd(), "pdv"),
    path.resolve(process.cwd(), "api"),
  ];
  const sqlJsPath = resolveNodeModule("sql.js", candidateRoots);
  if (!sqlJsPath) {
    throw new Error("sql.js was not found. Run npm install in pdv before analyzing PDV diagnostics.");
  }

  const sqlJs = await import(pathToFileURL(sqlJsPath).href);
  const initSqlJs = sqlJs.default || sqlJs;
  const wasmPath = path.basename(path.dirname(sqlJsPath)) === "dist"
    ? path.dirname(sqlJsPath)
    : path.join(path.dirname(sqlJsPath), "dist");
  const SQL = await initSqlJs({
    locateFile: (file) => path.join(wasmPath, file),
  });
  const database = new SQL.Database(fs.readFileSync(sqlitePath));

  return {
    all(sql, args = []) {
      const statement = database.prepare(sql);
      try {
        statement.bind(args);
        const rows = [];
        while (statement.step()) {
          rows.push(statement.getAsObject());
        }
        return rows;
      } finally {
        statement.free();
      }
    },
    close() {
      database.close();
    },
  };
}

function findCurrentPdvDatabase(packageDir) {
  return walkFiles(packageDir)
    .filter((filePath) => path.basename(filePath).toLowerCase() === "caixa-agil-pdv.sqlite")
    .sort((left, right) => fs.statSync(right).mtimeMs - fs.statSync(left).mtimeMs)[0] || null;
}

function findRustDeskStatus(packageDir) {
  return walkFiles(packageDir)
    .filter((filePath) => path.basename(filePath).toLowerCase() === "rustdesk-status.json")
    .sort((left, right) => fs.statSync(right).mtimeMs - fs.statSync(left).mtimeMs)[0] || null;
}

function findLogFiles(packageDir) {
  return walkFiles(packageDir)
    .filter((filePath) => /\.(log|jsonl)$/i.test(filePath))
    .map((filePath) => ({
      path: filePath,
      bytes: fs.statSync(filePath).size,
      updatedAt: fs.statSync(filePath).mtime.toISOString(),
    }))
    .sort((left, right) => right.updatedAt.localeCompare(left.updatedAt))
    .slice(0, 200);
}

function tableExists(db, tableName) {
  return db.all("SELECT name FROM sqlite_master WHERE type = 'table' AND name = ? LIMIT 1", [tableName]).length > 0;
}

function groupCounts(db, tableName, columnName) {
  if (!tableExists(db, tableName)) {
    return [];
  }

  return db.all(
    `SELECT ${columnName} AS status, COUNT(*) AS total, MAX(updated_at) AS last_updated_at FROM ${tableName} GROUP BY ${columnName} ORDER BY total DESC`,
  );
}

function redactValue(key, value) {
  if (/token|senha|password|secret|credential|credencial/i.test(String(key))) {
    return "[redacted]";
  }

  return value;
}

function readJsonIfExists(filePath) {
  if (!filePath || !fs.existsSync(filePath)) {
    return null;
  }

  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch {
    return null;
  }
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    printHelp();
    return;
  }

  const packageDir = args["package-dir"] || args._[0] ? path.resolve(String(args["package-dir"] || args._[0])) : null;
  const sqlitePath = args.sqlite
    ? path.resolve(String(args.sqlite))
    : packageDir
      ? findCurrentPdvDatabase(packageDir)
      : null;
  const limit = Math.max(1, Math.min(500, Number(args.limit || 100) || 100));

  if (!sqlitePath || !fs.existsSync(sqlitePath)) {
    throw new Error("Informe --sqlite ou --package-dir com caixa-agil-pdv.sqlite.");
  }

  const db = await createSqlJsDatabase(sqlitePath);
  let summary;
  try {
    const syncOutboxSummary = groupCounts(db, "sync_outbox", "status");
    const fiscalSyncSummary = groupCounts(db, "fiscal_documents", "sync_status");
    const fiscalStatusSummary = groupCounts(db, "fiscal_documents", "status");

    const pendingEvents = tableExists(db, "sync_outbox")
      ? db.all(
          `
            SELECT id, event_type, aggregate_type, aggregate_id, idempotency_key, attempts, created_at, updated_at, last_error
              FROM sync_outbox
             WHERE status IN ('pending', 'failed')
             ORDER BY updated_at DESC
             LIMIT ?
          `,
          [limit],
        )
      : [];

    const failedFiscalDocuments = tableExists(db, "fiscal_documents")
      ? db.all(
          `
            SELECT id, venda_id, command, ambiente, modelo, serie, numero, chave, status, sync_status, sync_attempts, sync_error, mensagem_operador, mensagem_tecnica, updated_at
              FROM fiscal_documents
             WHERE sync_status IN ('pending', 'failed') OR status IN ('erro', 'rejeitada', 'pendente')
             ORDER BY updated_at DESC
             LIMIT ?
          `,
          [limit],
        )
      : [];

    const metadata = tableExists(db, "pdv_metadata")
      ? db.all("SELECT key, value, updated_at FROM pdv_metadata ORDER BY key").map((row) => ({
          key: row.key,
          value: redactValue(row.key, row.value),
          updated_at: row.updated_at,
        }))
      : [];

    const stateScopes = tableExists(db, "pdv_state")
      ? db.all("SELECT scope, updated_at FROM pdv_state ORDER BY updated_at DESC LIMIT ?", [limit])
      : [];

    const rustdeskStatusPath = packageDir ? findRustDeskStatus(packageDir) : null;
    const rustdeskStatus = readJsonIfExists(rustdeskStatusPath);
    if (rustdeskStatus?.senha) {
      rustdeskStatus.senha = "[redacted]";
    }

    summary = {
      schema: "caixaagil-pdv-diagnostics-analysis.v1",
      analyzedAt: new Date().toISOString(),
      packageDir,
      sqlite: {
        path: sqlitePath,
        bytes: fs.statSync(sqlitePath).size,
        updatedAt: fs.statSync(sqlitePath).mtime.toISOString(),
      },
      syncOutboxSummary,
      fiscalSyncSummary,
      fiscalStatusSummary,
      pendingOrFailedEvents: pendingEvents,
      pendingOrFailedFiscalDocuments: failedFiscalDocuments,
      metadata,
      stateScopes,
      rustdeskStatusPath,
      rustdeskStatus,
      logFiles: packageDir ? findLogFiles(packageDir) : [],
    };
  } finally {
    db.close();
  }

  const outputPath = args.output ? path.resolve(String(args.output)) : packageDir ? path.join(packageDir, "pdv-diagnostics-analysis.json") : null;
  if (outputPath) {
    fs.writeFileSync(outputPath, `${JSON.stringify(summary, null, 2)}\n`, "utf8");
  }

  console.log(`Banco PDV analisado: ${sqlitePath}`);
  console.log("Fila local:");
  console.table(summary.syncOutboxSummary);
  console.log("Documentos fiscais por sync_status:");
  console.table(summary.fiscalSyncSummary);
  console.log(`Eventos pendentes/falhos listados: ${summary.pendingOrFailedEvents.length}`);
  console.log(`Documentos fiscais pendentes/falhos listados: ${summary.pendingOrFailedFiscalDocuments.length}`);
  if (outputPath) {
    console.log(`Analise salva em: ${outputPath}`);
  }
}

main().catch((error) => {
  console.error(error.stack || error.message);
  process.exit(1);
});
